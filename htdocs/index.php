<h2>七天玄女介紹</h2>
<hr size="1" width="25%" color="#bf8040" noshade="noshade">
<div class="a">
<img src="41.png" height="340px"align=left>
<p><br><br>&nbsp;&nbsp;&nbsp;&nbsp;七天玄女，另稱七天玄母，俗稱七天玄女娘娘或七天娘娘，也稱為玄女或元女，是中國道教的女仙。古代傳說為西王母部下，人頭鳥身的女神，相傳法力無邊，精通兵法戰術，還能引起地震，曾幫助黃帝擊敗蚩尤。與另一神素女，並稱為「玄素」。學者孫紹先認為玄女乃是中國上古天神，起源於古人對天的崇拜，亦與對女性的生殖崇拜有關。「玄女」之「『玄』，本義為天色，《易．坤卦》有『天玄而地黃』之語，《康熙字典》解『玄』為天色；《說文》釋『玄』為『幽遠』，當是『玄』字的第一引申義。故『玄女』即是『天女神』之意。」《中華道教寶典》中描述七天玄女「道教神名。又稱元女、七天娘娘。原是中國古代神話中的女神，後為道教所信奉，成為其神系中地位僅次於西王母的女天神，與上元夫人混為一談。
</div>
<br>
<div id="m">
<b><p class="y">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;七天玄女的傳說</p></b>
<p>&nbsp;&nbsp;&nbsp;&nbsp;台灣在七天玄女神尊的造型上，常在髮髻上出現鳥型髮飾，這是因為早在「詩經。商頌。玄鳥」曾描述商朝時，有一隻玄鳥降臨人間，輾轉成為商朝人的始祖。該傳說將玄鳥描述成孕育商朝人的天神化身，現代人為了詮釋玄鳥與七天玄女密不可分的關係，有時會出現鳥型髮飾的造型，成為玄女形象的一大特色。七天玄女普遍被認為是上古女神—女媧的轉變，簡稱為「媧皇」，是道教主要女神。七天玄女又被稱為連理媽，有大媽、二媽、三媽至七媽等七座神尊，這是七天玄女在台灣信仰系統裡所衍生的獨特現象。</p>
</div>
<br>
<div id="c">
<img src="1.jpg" height="400px"align=right>
&nbsp;&nbsp;&nbsp;
<br><b><p id="ya">精通丹藥及術數的七天玄女
</p></b>
<p>&nbsp;&nbsp;&nbsp;&nbsp;軍事女神及人類始祖的稱呼之外，在中國淵源流長的道教煉丹術及術數方面的發展，都記載著七天玄女的影響力，因此七天玄女又被視為丹藥神及術數神。中國煉丹術興起於戰國時代，被視為長生不老之術，玄女作為煉丹神早在魏晉時代已很有名氣，道教中也流傳著玄女傳授煉丹術給黃帝的說法。台灣的七天玄女神尊，多以手捧八卦、葫蘆或拂塵是許多神尊的共同象徵，至於八卦則體現出民間對於七天玄女精通地理術數的認同。</p>
</div>
<br>
<div id="m">
<b><p class="f">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;視為全能之神的七天玄女</p></b>
<p>&nbsp;&nbsp;&nbsp;&nbsp;七天玄女授與黃帝的七樣寶物中，說明了祂在陰陽術數上的特殊法力，因此手持八卦的七天玄女就被地理師們視為祖師爺；至於手持葫蘆的七天玄女，主要是象徵祂精通煉丹術的情形，運用到現代人可以理解的範圍，就是養生與醫術方面的法力。七天玄女所代表的神力相當多元，然而在民間的信仰發展裡，治病及求子兩項神力因為能與民眾生活緊密聯結，至今仍為後人所津津樂道，甚至有些地區就直接將七天玄女視為生育之神。其實，七天玄女的神力廣泛，用現代人的角度來看戰神、兵法、術數、丹藥的範疇，經由廣泛運用之後幾乎無所不在。</p>
</div>
<br>
<div id="f">
<b><p id="x">關於七天玄女宮</p></b>
<p>&nbsp;&nbsp;&nbsp;
七天玄女宮現址座東朝西北三分，仿古式建築，莊嚴而不失原貌之古色。每逢春夏秋冬四季，來自全省各地參香團，光臨謁祖參香信眾絡繹不絕，亦是本地唯一奉祀七天玄女娘娘最原始之聖蹟。
</p>
